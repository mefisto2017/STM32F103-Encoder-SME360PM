// ~~~~~~~~~~~~~~~~~~  CAN VARIABLES  ~~~~~~~~~~~~~~~~~~
CAN_TxHeaderTypeDef TxHeader;
CAN_RxHeaderTypeDef RxHeader;

uint8_t TxData[8];
uint8_t RxData[8];

uint32_t TxMailbox;

int datacheck = 0;

// ~~~~~~~~~~~~~~~~~~  ADC VARIABLES  ~~~~~~~~~~~~~~~~~~
volatile uint16_t adcResultsDMA[4];
const int adcChannelCount = sizeof(adcResultsDMA) / sizeof(adcResultsDMA[0]);
volatile int adcConversionComplete = 0;


// ~~~~~~~~~~~~~~~~~~  CAN INTERRUPT  ~~~~~~~~~~~~~~~~~~
void HAL_CAN_RxFifo1MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
	HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO1, &RxHeader, RxData);
	if (RxHeader.DLC == 1 && RxData[0] == 1) // Check if only 1 byte of data && if it is requesting data
	{
		datacheck = 1;
	}
}

// ~~~~~~~~~~~~~~~~~~  ADC INTERRUPT  ~~~~~~~~~~~~~~~~~~
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	adcConversionComplete = 1;
}

// ~~~~~~~~~~~~~~~~~~  TRANSFORM INT TO BYTE  ~~~~~~~~~~~~~~~~~~
void intTobyte(void)
{
	for (int i =0; i < 4; i++)
	{
		TxData[2*i] = (adcResultsDMA[i] >> 8) & 0xff; // big byte
		TxData[2*i+1] = adcResultsDMA[i] & 0xff;      // little byte
	}

}

